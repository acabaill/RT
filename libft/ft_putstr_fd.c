#include "libft.h"

int		ft_putstr_fd(char const *s, int fd)
{
	int		i;

	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		++i;
	}
	if (fd == 2)
		return (-1);
	return (i);
}
